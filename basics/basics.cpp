# include <iostream>
using namespace std;  // this will help us to not write namespace again and again because in real we have to write std::cout<<"" but using name space we can write it as cout<<"".

int main(){
    std::cout<<"Hello world!\n"; // after using namespace
    cout<<"hello world!"<<endl; // c - console, out - output, endl - end line
    cout<<3;
    return 0;
}